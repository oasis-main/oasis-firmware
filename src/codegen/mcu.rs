fn generate_control_module(config: &DeviceConfig, src_dir: &Path) -> Result<()> {
    let control_tasks: Vec<String> = config.control_loops.iter()
        .map(|ctrl| generate_control_task(ctrl))
        .collect();
    
    let content = format!(r#"//! Control loop implementations

use embassy_time::{{Duration, Timer}};
use crate::sensors::READINGS;
use crate::actuators;
use defmt::*;

{control_tasks}
"#,
        control_tasks = control_tasks.join("\n\n"),
    );
    
    fs::write(src_dir.join("control.rs"), content)?;
    Ok(())
}

fn generate_control_task(ctrl: &ControlLoop) -> String {
    let task_name = ctrl.name.replace('-', "_");
    
    match ctrl.loop_type {
        ControlLoopType::Threshold => {
            let threshold = ctrl.threshold.as_ref().unwrap();
            format!(r#"#[embassy_executor::task]
pub async fn {task_name}() {{
    const SETPOINT: f32 = {setpoint};
    const HYSTERESIS: f32 = {hysteresis};
    const INPUT_SENSOR: &str = "{sensor}";
    const INPUT_MEASUREMENT: &str = "{measurement}";
    const OUTPUT_ACTUATOR: &str = "{actuator}";
    
    let mut state = false;
    
    loop {{
        // Get latest reading for our sensor
        let reading = READINGS.receive().await;
        
        if reading.sensor == INPUT_SENSOR && reading.measurement == INPUT_MEASUREMENT {{
            let value = reading.value;
            
            // Threshold with hysteresis
            let new_state = match "{direction}" {{
                "cool" => value > SETPOINT + HYSTERESIS,
                "heat" => value < SETPOINT - HYSTERESIS,
                _ => state,
            }};
            
            if new_state != state {{
                state = new_state;
                actuators::set_state(OUTPUT_ACTUATOR, state);
                info!("{{}} -> {{}}", OUTPUT_ACTUATOR, state);
            }}
        }}
    }}
}}"#,
                task_name = task_name,
                setpoint = threshold.setpoint,
                hysteresis = threshold.hysteresis,
                sensor = ctrl.input.sensor,
                measurement = ctrl.input.measurement,
                actuator = ctrl.output.actuator,
                direction = format!("{:?}", threshold.direction).to_lowercase(),
            )
        }
        ControlLoopType::Pid => {
            let pid = ctrl.pid.as_ref().unwrap();
            format!(r#"#[embassy_executor::task]
pub async fn {task_name}() {{
    const KP: f32 = {kp};
    const KI: f32 = {ki};
    const KD: f32 = {kd};
    const SETPOINT: f32 = {setpoint};
    const OUTPUT_MIN: f32 = {output_min};
    const OUTPUT_MAX: f32 = {output_max};
    const SAMPLE_TIME_MS: u32 = {sample_time};
    
    let mut integral: f32 = 0.0;
    let mut prev_error: f32 = 0.0;
    
    loop {{
        let reading = READINGS.receive().await;
        
        if reading.sensor == "{sensor}" && reading.measurement == "{measurement}" {{
            let error = SETPOINT - reading.value;
            
            // PID calculation
            integral += error * (SAMPLE_TIME_MS as f32 / 1000.0);
            let derivative = (error - prev_error) / (SAMPLE_TIME_MS as f32 / 1000.0);
            prev_error = error;
            
            let mut output = KP * error + KI * integral + KD * derivative;
            output = output.clamp(OUTPUT_MIN, OUTPUT_MAX);
            
            actuators::set_value("{actuator}", output as u16);
        }}
        
        Timer::after(Duration::from_millis(SAMPLE_TIME_MS as u64)).await;
    }}
}}"#,
                task_name = task_name,
                kp = pid.kp,
                ki = pid.ki,
                kd = pid.kd,
                setpoint = pid.setpoint,
                output_min = pid.output_min,
                output_max = pid.output_max,
                sample_time = pid.sample_time_ms,
                sensor = ctrl.input.sensor,
                measurement = ctrl.input.measurement,
                actuator = ctrl.output.actuator,
            )
        }
        _ => format!(r#"#[embassy_executor::task]
pub async fn {task_name}() {{
    // TODO: Implement {loop_type:?} control loop
    loop {{
        Timer::after(Duration::from_secs(1)).await;
    }}
}}"#, task_name = task_name, loop_type = ctrl.loop_type),
    }
}

fn generate_comms_module(config: &DeviceConfig, src_dir: &Path) -> Result<()> {
    let content = format!(r#"//! Communication (MQTT/HTTPS) module

use embassy_time::{{Duration, Timer}};
use crate::sensors::READINGS;
use defmt::*;

const DEVICE_ID: &str = "{device_id}";
{mqtt_config}
{https_config}

pub async fn init(spawner: &Spawner, peripherals: &Peripherals) {{
    // TODO: Initialize WiFi and connect
    info!("Initializing communications for {{}}", DEVICE_ID);
}}

#[embassy_executor::task]
pub async fn publish_task() {{
    loop {{
        // Collect readings and publish
        let reading = READINGS.receive().await;
        
        // TODO: Publish via MQTT or batch for HTTPS
        info!("Publish: {{}}:{{}} = {{}}", reading.sensor, reading.measurement, reading.value);
        
        Timer::after(Duration::from_millis({publish_interval})).await;
    }}
}}
"#,
        device_id = config.device.id,
        mqtt_config = generate_mqtt_config(config),
        https_config = generate_https_config(config),
        publish_interval = config.data_publishing
            .as_ref()
            .and_then(|dp| dp.mqtt.as_ref())
            .map(|m| m.publish_interval_ms)
            .unwrap_or(5000),
    );
    
    fs::write(src_dir.join("comms.rs"), content)?;
    Ok(())
}

fn generate_mqtt_config(config: &DeviceConfig) -> String {
    if let Some(mqtt) = config.connectivity.mqtt.as_ref() {
        format!(r#"
const MQTT_BROKER: &str = "{}";
const MQTT_PORT: u16 = {};
const MQTT_USE_TLS: bool = {};"#,
            mqtt.broker, mqtt.port, mqtt.use_tls)
    } else {
        String::new()
    }
}

fn generate_https_config(config: &DeviceConfig) -> String {
    if let Some(https) = config.connectivity.https.as_ref() {
        format!(r#"
const HTTPS_ENDPOINT: &str = "{}";"#, https.endpoint)
    } else {
        String::new()
    }
}

fn generate_build_rs(_config: &DeviceConfig, output_dir: &Path) -> Result<()> {
    let content = r#"//! Build script for firmware

fn main() {
    // Link scripts, etc.
    println!("cargo:rerun-if-changed=build.rs");
}
"#;
    
    fs::write(output_dir.join("build.rs"), content)?;
    Ok(())
}

fn generate_cargo_config(config: &DeviceConfig, output_dir: &Path) -> Result<()> {
    let cargo_dir = output_dir.join(".cargo");
    fs::create_dir_all(&cargo_dir)?;
    
    let target = match config.device.board.model.as_str() {
        "esp32_devkit" => "xtensa-esp32-none-elf",
        "esp32_c3" => "riscv32imc-unknown-none-elf",
        "esp32_s3" => "xtensa-esp32s3-none-elf",
        "stm32f103" => "thumbv7m-none-eabi",
        "stm32f411" => "thumbv7em-none-eabihf",
        _ => "thumbv7em-none-eabihf",
    };
    
    let content = format!(r#"[build]
target = "{target}"

[target.{target}]
runner = "probe-rs run --chip auto"

[env]
DEFMT_LOG = "info"
"#, target = target);
    
    fs::write(cargo_dir.join("config.toml"), content)?;
    Ok(())
}

fn needs_mqtt(config: &DeviceConfig) -> bool {
    matches!(config.connectivity.mode, 
        ConnectivityMode::DirectMqtt | ConnectivityMode::Hybrid)
}
