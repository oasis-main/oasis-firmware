//! MCU (ESP32, STM32, etc.) code generation
//!
//! Generates embedded Rust firmware using embassy or esp-idf-hal

use crate::config::*;
use crate::error::{OasisError, Result};
use std::path::Path;
use std::fs;

/// Generate MCU firmware project
pub fn generate(config: &DeviceConfig, output_dir: &Path) -> Result<()> {
    tracing::info!("Generating MCU firmware for {}", config.device.id);
    
    // Create output directory structure
    let src_dir = output_dir.join("src");
    fs::create_dir_all(&src_dir)?;
    
    // Generate Cargo.toml
    generate_cargo_toml(config, output_dir)?;
    
    // Generate main.rs
    generate_main_rs(config, &src_dir)?;
    
    // Generate sensor modules
    generate_sensors_module(config, &src_dir)?;
    
    // Generate actuator modules
    generate_actuators_module(config, &src_dir)?;
    
    // Generate control loop module
    generate_control_module(config, &src_dir)?;
    
    // Generate communication module
    generate_comms_module(config, &src_dir)?;
    
    // Generate build.rs if needed
    generate_build_rs(config, output_dir)?;
    
    // Generate .cargo/config.toml
    generate_cargo_config(config, output_dir)?;
    
    tracing::info!("MCU firmware generated at {:?}", output_dir);
    Ok(())
}

fn generate_cargo_toml(config: &DeviceConfig, output_dir: &Path) -> Result<()> {
    let board = &config.device.board.model;
    
    // Determine HAL crate based on board
    let (hal_crate, hal_features) = match board.as_str() {
        "esp32_devkit" | "esp32_c3" | "esp32_s3" => {
            ("esp-idf-hal", vec!["embassy"])
        }
        "stm32f103" => ("stm32f1xx-hal", vec!["stm32f103", "rt"]),
        "stm32f411" => ("stm32f4xx-hal", vec!["stm32f411", "rt"]),
        "teensy_40" | "teensy_41" => ("teensy4-bsp", vec![]),
        _ => return Err(OasisError::UnsupportedBoard(board.clone())),
    };
    
    let content = format!(r#"[package]
name = "{device_id}-firmware"
version = "0.1.0"
edition = "2021"

[dependencies]
# HAL
{hal_crate} = {{ version = "*", features = {hal_features:?} }}

# Async runtime
embassy-executor = {{ version = "0.5", features = ["arch-xtensa", "executor-thread"] }}
embassy-time = "0.3"
embassy-sync = "0.5"

# Serialization
serde = {{ version = "1.0", default-features = false, features = ["derive"] }}
postcard = {{ version = "1.0", features = ["alloc"] }}

# Logging
defmt = "0.3"
defmt-rtt = "0.4"

# MQTT (if enabled)
{mqtt_dep}

# Error handling
embedded-hal = "1.0"

[profile.release]
opt-level = "s"
lto = true
debug = false

[profile.dev]
opt-level = 1
debug = true
"#,
        device_id = config.device.id.replace('-', "_"),
        hal_crate = hal_crate,
        hal_features = hal_features,
        mqtt_dep = if needs_mqtt(config) {
            r#"embedded-mqtt = "0.1""#
        } else {
            "# embedded-mqtt = \"0.1\"  # Uncomment if MQTT needed"
        },
    );
    
    fs::write(output_dir.join("Cargo.toml"), content)?;
    Ok(())
}

fn generate_main_rs(config: &DeviceConfig, src_dir: &Path) -> Result<()> {
    let content = format!(r#"//! {device_name} Firmware
//! 
//! Auto-generated by oasis-build from device.yaml
//! Device ID: {device_id}

#![no_std]
#![no_main]

mod sensors;
mod actuators;
mod control;
mod comms;

use embassy_executor::Spawner;
use defmt::*;
use defmt_rtt as _;

// Device configuration (compiled in)
const DEVICE_ID: &str = "{device_id}";

#[embassy_executor::main]
async fn main(spawner: Spawner) {{
    info!("Starting {{}} firmware", DEVICE_ID);
    
    // Initialize hardware
    let peripherals = embassy_hal::init(Default::default());
    
    // Initialize sensors
    let sensors = sensors::init(&peripherals);
    info!("Initialized {{}} sensors", sensors.len());
    
    // Initialize actuators
    let actuators = actuators::init(&peripherals);
    info!("Initialized {{}} actuators", actuators.len());
    
    // Initialize communication
    comms::init(&spawner, &peripherals).await;
    
    // Spawn sensor reading tasks
    for sensor in sensors {{
        spawner.spawn(sensors::read_task(sensor)).unwrap();
    }}
    
    // Spawn control loop tasks
    {control_spawns}
    
    // Spawn communication task
    spawner.spawn(comms::publish_task()).unwrap();
    
    info!("{{}} firmware running", DEVICE_ID);
    
    // Main loop - handle watchdog
    loop {{
        embassy_time::Timer::after_millis(1000).await;
        // Feed watchdog here
    }}
}}

#[panic_handler]
fn panic(info: &core::panic::PanicInfo) -> ! {{
    defmt::error!("Panic: {{}}", defmt::Debug2Format(info));
    loop {{}}
}}
"#,
        device_name = config.device.name,
        device_id = config.device.id,
        control_spawns = generate_control_spawns(config),
    );
    
    fs::write(src_dir.join("main.rs"), content)?;
    Ok(())
}

fn generate_control_spawns(config: &DeviceConfig) -> String {
    config.control_loops.iter()
        .map(|ctrl| format!(
            "spawner.spawn(control::{}()).unwrap();",
            ctrl.name.replace('-', "_")
        ))
        .collect::<Vec<_>>()
        .join("\n    ")
}

fn generate_sensors_module(config: &DeviceConfig, src_dir: &Path) -> Result<()> {
    let sensor_inits: Vec<String> = config.sensors.iter()
        .map(|s| generate_sensor_init(s))
        .collect();
    
    let content = format!(r#"//! Sensor drivers and reading tasks

use embassy_sync::channel::Channel;
use embassy_time::{{Duration, Timer}};
use defmt::*;

/// Sensor reading channel (sensor_name, measurement_name, value)
pub static READINGS: Channel<CriticalSectionRawMutex, SensorReading, 32> = Channel::new();

#[derive(Clone, Debug)]
pub struct SensorReading {{
    pub sensor: &'static str,
    pub measurement: &'static str,
    pub value: f32,
    pub timestamp_ms: u64,
}}

pub struct Sensor {{
    pub name: &'static str,
    pub interval_ms: u32,
    // Add sensor-specific fields
}}

pub fn init(peripherals: &Peripherals) -> Vec<Sensor> {{
    let mut sensors = Vec::new();
    
    {sensor_inits}
    
    sensors
}}

#[embassy_executor::task]
pub async fn read_task(sensor: Sensor) {{
    loop {{
        // Read sensor
        let reading = read_sensor(&sensor).await;
        
        // Send to channel
        READINGS.send(reading).await;
        
        // Wait for next interval
        Timer::after(Duration::from_millis(sensor.interval_ms as u64)).await;
    }}
}}

async fn read_sensor(sensor: &Sensor) -> SensorReading {{
    // TODO: Implement actual sensor reading based on type
    SensorReading {{
        sensor: sensor.name,
        measurement: "value",
        value: 0.0,
        timestamp_ms: embassy_time::Instant::now().as_millis(),
    }}
}}
"#,
        sensor_inits = sensor_inits.join("\n    "),
    );
    
    fs::write(src_dir.join("sensors.rs"), content)?;
    Ok(())
}

fn generate_sensor_init(sensor: &Sensor) -> String {
    format!(r#"// {name}: {sensor_type:?}
    sensors.push(Sensor {{
        name: "{name}",
        interval_ms: {interval},
    }});"#,
        name = sensor.name,
        sensor_type = sensor.sensor_type,
        interval = sensor.sampling.interval_ms,
    )
}

