//! Raspberry Pi code generation
//!
//! Generates a Rust supervisor daemon for RPi platforms

use crate::config::*;
use crate::error::Result;
use std::path::Path;
use std::fs;

/// Generate RPi supervisor project
pub fn generate(config: &DeviceConfig, output_dir: &Path) -> Result<()> {
    tracing::info!("Generating RPi supervisor for {}", config.device.id);
    
    // Create output directory structure
    let src_dir = output_dir.join("src");
    fs::create_dir_all(&src_dir)?;
    
    // Generate Cargo.toml
    generate_cargo_toml(config, output_dir)?;
    
    // Generate main.rs
    generate_main_rs(config, &src_dir)?;
    
    // Generate config module
    generate_config_module(config, &src_dir)?;
    
    // Generate sensors module
    generate_sensors_module(config, &src_dir)?;
    
    // Generate actuators module
    generate_actuators_module(config, &src_dir)?;
    
    // Generate MQTT module
    generate_mqtt_module(config, &src_dir)?;
    
    // Generate serial bridge module (for gateway mode)
    generate_serial_module(config, &src_dir)?;
    
    // Generate systemd service file
    generate_systemd_service(config, output_dir)?;
    
    tracing::info!("RPi supervisor generated at {:?}", output_dir);
    Ok(())
}

