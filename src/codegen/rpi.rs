fn generate_mqtt_module(config: &DeviceConfig, src_dir: &Path) -> Result<()> {
    let _device_id = &config.device.id;
    let broker = config.connectivity.mqtt.as_ref()
        .map(|m| m.broker.as_str()).unwrap_or("localhost");
    let port = config.connectivity.mqtt.as_ref()
        .map(|m| m.port).unwrap_or(8883);

    // Collect all actuator command topics so we subscribe to them on connect
    let cmd_topic_subs: String = config.actuators.iter().map(|a| {
        format!(
            "    client.subscribe(format!(\"oasis/devices/{{}}/{name}/cmd\", device_id), QoS::AtLeastOnce).await?;\n",
            name = a.name,
        )
    }).collect();

    let content = format!(r#"//! MQTT client — generated from device.yaml
//! Absorbed from legacy rusty_pipes.custom_signal: JSON-file IPC → MQTT topics
//! Absorbed from legacy rusty_pipes.fast_mutx: JSON-file mutex → tokio channel cmd queue

use anyhow::{{Context, Result}};
use rumqttc::{{AsyncClient, Event, MqttOptions, Packet, Publish, QoS}};
use tokio::sync::{{mpsc, Mutex}};
use std::{{collections::HashMap, sync::Arc}};

/// Commands received from oasis-cloud, keyed by topic.
/// Replaces rusty_pipes.custom_signal JSON file signals and fast_mutx locks —
/// MQTT topics are the coordination primitive, tokio channels are the mutex.
type CmdQueue = Arc<Mutex<HashMap<String, Vec<String>>>>;

/// Shared MQTT client used by sensors.rs, actuators.rs, and serial.rs.
/// Wraps rumqttc AsyncClient + an inbound command queue.
pub struct MqttClient {{
    client: AsyncClient,
    cmd_queue: CmdQueue,
    device_id: String,
}}

impl MqttClient {{
    /// Publish raw bytes to a topic (telemetry path for sensors/serial)
    pub async fn publish_bytes(&self, topic: &str, payload: Vec<u8>) -> Result<()> {{
        self.client
            .publish(topic, QoS::AtLeastOnce, false, payload)
            .await
            .context("MQTT publish failed")
    }}

    /// Publish a JSON-serializable value as telemetry
    pub async fn publish(&self, topic: &str, payload: Vec<u8>) -> Result<()> {{
        self.publish_bytes(topic, payload).await
    }}

    /// Drain one command for the given topic (non-blocking).
    /// Replaces rusty_pipes fast_mutx lock/unlock — caller owns the value once dequeued.
    pub async fn poll_cmd(&self, topic: &str) -> Option<String> {{
        self.cmd_queue.lock().await
            .get_mut(topic)
            .and_then(|q| if q.is_empty() {{ None }} else {{ Some(q.remove(0)) }})
    }}
}}

/// Connect to MQTT, subscribe to all actuator cmd topics, and return a shared client.
pub async fn connect(config: &super::config::AppConfig) -> Result<Arc<Mutex<MqttClient>>> {{
    let device_id = config.device_id.clone();
    let mut opts = MqttOptions::new(&device_id, "{broker}", {port});
    opts.set_keep_alive(std::time::Duration::from_secs(30));
    opts.set_clean_session(true);

    let (client, mut eventloop) = AsyncClient::new(opts, 128);

    // Subscribe to all actuator command topics
{cmd_topic_subs}
    tracing::info!("MQTT connecting to {broker}:{port} as {{}}", device_id);

    let cmd_queue: CmdQueue = Arc::new(Mutex::new(HashMap::new()));
    let queue_ref = cmd_queue.clone();

    // Event loop: drive rumqttc and funnel inbound Publish packets into cmd_queue.
    // Replaces rusty_pipes.custom_signal + fast_mutx: no JSON files, no fs::write races.
    tokio::spawn(async move {{
        loop {{
            match eventloop.poll().await {{
                Ok(Event::Incoming(Packet::Publish(Publish {{ topic, payload, .. }}))) => {{
                    let val = String::from_utf8_lossy(&payload).trim().to_string();
                    tracing::debug!("CMD {{}}: {{}}", topic, val);
                    queue_ref.lock().await
                        .entry(topic.to_string())
                        .or_default()
                        .push(val);
                }}
                Ok(Event::Incoming(Packet::ConnAck(_))) => {{
                    tracing::info!("MQTT connected");
                }}
                Ok(_) => {{}}
                Err(e) => {{
                    tracing::error!("MQTT error: {{:?}}", e);
                    tokio::time::sleep(tokio::time::Duration::from_secs(5)).await;
                }}
            }}
        }}
    }});

    Ok(Arc::new(Mutex::new(MqttClient {{
        client,
        cmd_queue,
        device_id,
    }})))
}}
"#,
        broker = broker,
        port = port,
        cmd_topic_subs = cmd_topic_subs,
    );

    fs::write(src_dir.join("mqtt.rs"), content)?;
    Ok(())
}

fn generate_serial_module(config: &DeviceConfig, src_dir: &Path) -> Result<()> {
    let baud = config.connectivity.serial.as_ref()
        .map(|s| s.baud_rate)
        .unwrap_or(115200);
    let protocol = config.connectivity.serial.as_ref()
        .map(|s| s.protocol)
        .unwrap_or_default();
    let framing_comment = match protocol {
        SerialProtocol::CobsMsgpack => "COBS-framed MessagePack",
        SerialProtocol::CobsJson    => "COBS-framed JSON",
        SerialProtocol::RawJson     => "newline-delimited JSON",
    };

    let content = format!(r#"//! Serial bridge — generated from device.yaml
//! Absorbed from legacy rusty_pipes.Open: managed child process → tokio_serial async task
//! Absorbed from legacy rusty_pipes.custom_signal: JSON file signals → MQTT cmd topics
//! Framing: {framing_comment}

use anyhow::{{Context, Result}};
use tokio_serial::{{SerialPortBuilderExt, SerialStream}};
use tokio::io::{{AsyncReadExt, AsyncWriteExt, BufReader}};
use tokio::sync::Mutex;
use std::sync::Arc;

/// Decoded message from an MCU minion over serial
#[derive(Debug, serde::Deserialize)]
pub struct MinonMessage {{
    pub source: Option<String>,
    pub kind: Option<String>,
    pub payload: serde_json::Value,
}}

/// Open the serial port and bridge minion telemetry → MQTT
/// Replaces rusty_pipes.Open subprocess wrapper: the port is owned by this async task,
/// no child process needed. Shutdown is via tokio cancellation token / ctrl-c.
pub async fn run(
    mqtt: Arc<Mutex<super::mqtt::MqttClient>>,
    config: &super::config::AppConfig,
) -> Result<()> {{
    let serial_cfg = config.serial.as_ref()
        .context("Serial not configured but serial bridge was spawned")?;

    let port = tokio_serial::new(&serial_cfg.port, {baud})
        .open_native_async()
        .with_context(|| format!("Failed to open {{}}", &serial_cfg.port))?;
    tracing::info!("Serial bridge open: {{}} @ {baud} baud", &serial_cfg.port);

    let mut reader = BufReader::new(port);
    let mut frame_buf = Vec::with_capacity(256);

    loop {{
        // Read until newline (RawJson) or COBS end-of-frame (0x00)
        let delimiter: u8 = {delimiter};
        match reader.read_until_byte(delimiter, &mut frame_buf).await {{
            Ok(0) => {{
                tracing::warn!("Serial EOF — MCU disconnected?");
                tokio::time::sleep(tokio::time::Duration::from_secs(2)).await;
                frame_buf.clear();
                continue;
            }}
            Ok(_) => {{}}
            Err(e) => {{
                tracing::error!("Serial read error: {{:?}}", e);
                frame_buf.clear();
                continue;
            }}
        }}

        // Decode frame
        let payload_bytes = frame_buf.trim_ascii();
        let msg: Result<MinonMessage, _> = serde_json::from_slice(payload_bytes);
        frame_buf.clear();

        match msg {{
            Ok(m) => {{
                let topic = format!(
                    "oasis/devices/{{}}/minion/{{}}/telemetry",
                    config.device_id,
                    m.source.as_deref().unwrap_or("unknown"),
                );
                let json = serde_json::to_vec(&m.payload).unwrap_or_default();
                if let Err(e) = mqtt.lock().await.publish_bytes(&topic, json).await {{
                    tracing::warn!("MQTT publish failed: {{:?}}", e);
                }}
            }}
            Err(e) => {{
                tracing::debug!("Serial frame parse error: {{:?}}", e);
            }}
        }}
    }}
}}
"#,
        framing_comment = framing_comment,
        baud = baud,
        delimiter = match protocol {
            SerialProtocol::RawJson  => 0x0Au8, // '\n'
            _                        => 0x00u8, // COBS end-of-frame
        },
    );

    fs::write(src_dir.join("serial.rs"), content)?;
    Ok(())
}

