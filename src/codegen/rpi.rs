fn generate_systemd_service(config: &DeviceConfig, output_dir: &Path) -> Result<()> {
    let service_name = config.device.id.replace('-', "_");
    
    let content = format!(r#"[Unit]
Description={device_name} Supervisor
After=network-online.target
Wants=network-online.target

[Service]
Type=simple
User=oasis
Group=oasis
WorkingDirectory=/opt/oasis/{device_id}
ExecStart=/opt/oasis/{device_id}/{service_name}-supervisor
Restart=always
RestartSec=5
Environment="RUST_LOG=info"

[Install]
WantedBy=multi-user.target
"#,
        device_name = config.device.name,
        device_id = config.device.id,
        service_name = service_name,
    );
    
    fs::write(output_dir.join(format!("{}.service", service_name)), content)?;
    Ok(())
}
