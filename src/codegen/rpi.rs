fn generate_main_rs(config: &DeviceConfig, src_dir: &Path) -> Result<()> {
    let content = format!(r#"//! {device_name} Supervisor
//!
//! Auto-generated by oasis-build from device.yaml
//! Device ID: {device_id}

mod config;
mod sensors;
mod actuators;
mod mqtt;
mod serial;

use anyhow::Result;
use tracing::{{info, Level}};
use tracing_subscriber::EnvFilter;

#[tokio::main]
async fn main() -> Result<()> {{
    // Initialize logging
    tracing_subscriber::fmt()
        .with_env_filter(
            EnvFilter::from_default_env()
                .add_directive(Level::INFO.into())
        )
        .init();
    
    info!("Starting {device_id} supervisor");
    
    // Load configuration
    let config = config::load()?;
    info!("Loaded configuration for device: {{}}", config.device_id);
    
    // Initialize hardware
    let gpio = rppal::gpio::Gpio::new()?;
    
    // Initialize sensors
    let sensors = sensors::init(&gpio, &config).await?;
    info!("Initialized {{}} sensors", sensors.len());
    
    // Initialize actuators
    let actuators = actuators::init(&gpio, &config).await?;
    info!("Initialized {{}} actuators", actuators.len());
    
    // Start MQTT connection
    let mqtt_handle = mqtt::start(&config).await?;
    
    // Start serial bridge (if gateway mode)
    {serial_init}
    
    // Run the supervisor
    tokio::select! {{
        res = sensors::run_all(sensors, mqtt_handle.clone()) => {{
            if let Err(e) = res {{
                tracing::error!("Sensor task failed: {{:?}}", e);
            }}
        }}
        res = mqtt::run(mqtt_handle) => {{
            if let Err(e) = res {{
                tracing::error!("MQTT task failed: {{:?}}", e);
            }}
        }}
        _ = tokio::signal::ctrl_c() => {{
            info!("Shutdown signal received");
        }}
    }}
    
    info!("{device_id} supervisor stopped");
    Ok(())
}}
"#,
        device_name = config.device.name,
        device_id = config.device.id,
        serial_init = if matches!(config.connectivity.mode, ConnectivityMode::Hybrid | ConnectivityMode::SerialGateway) {
            r#"let serial_handle = serial::start(&config).await?;
    tokio::spawn(serial::run(serial_handle, mqtt_handle.clone()));"#
        } else {
            "// Serial bridge not enabled for this connectivity mode"
        },
    );
    
    fs::write(src_dir.join("main.rs"), content)?;
    Ok(())
}

fn generate_config_module(config: &DeviceConfig, src_dir: &Path) -> Result<()> {
    let content = format!(r#"//! Configuration loading

use anyhow::Result;
use serde::Deserialize;

#[derive(Debug, Deserialize)]
pub struct AppConfig {{
    pub device_id: String,
    pub mqtt: MqttConfig,
    pub https: Option<HttpsConfig>,
    pub serial: Option<SerialConfig>,
}}

#[derive(Debug, Deserialize)]
pub struct MqttConfig {{
    pub broker: String,
    pub port: u16,
    pub use_tls: bool,
    pub telemetry_topic: String,
    pub cmd_topic: String,
}}

#[derive(Debug, Deserialize)]
pub struct HttpsConfig {{
    pub endpoint: String,
    pub batch_interval_s: u32,
}}

#[derive(Debug, Deserialize)]
pub struct SerialConfig {{
    pub port: String,
    pub baud_rate: u32,
}}

pub fn load() -> Result<AppConfig> {{
    // Load .env file if present
    dotenvy::dotenv().ok();
    
    // Build configuration
    let config = AppConfig {{
        device_id: "{device_id}".into(),
        mqtt: MqttConfig {{
            broker: std::env::var("MQTT_BROKER").unwrap_or_else(|_| "{mqtt_broker}".into()),
            port: std::env::var("MQTT_PORT")
                .ok()
                .and_then(|s| s.parse().ok())
                .unwrap_or({mqtt_port}),
            use_tls: {mqtt_tls},
            telemetry_topic: "oasis/devices/{device_id}/telemetry".into(),
            cmd_topic: "oasis/devices/{device_id}/cmd".into(),
        }},
        https: {https_config},
        serial: {serial_config},
    }};
    
    Ok(config)
}}
"#,
        device_id = config.device.id,
        mqtt_broker = config.connectivity.mqtt.as_ref().map(|m| m.broker.as_str()).unwrap_or("localhost"),
        mqtt_port = config.connectivity.mqtt.as_ref().map(|m| m.port).unwrap_or(8883),
        mqtt_tls = config.connectivity.mqtt.as_ref().map(|m| m.use_tls).unwrap_or(true),
        https_config = if config.connectivity.https.is_some() {
            format!(r#"Some(HttpsConfig {{
            endpoint: "{}".into(),
            batch_interval_s: {},
        }})"#, 
                config.connectivity.https.as_ref().unwrap().endpoint,
                config.data_publishing.as_ref()
                    .and_then(|dp| dp.https.as_ref())
                    .map(|h| h.batch_interval_s)
                    .unwrap_or(60)
            )
        } else {
            "None".into()
        },
        serial_config = if config.connectivity.serial.is_some() {
            format!(r#"Some(SerialConfig {{
            port: "/dev/ttyUSB0".into(),
            baud_rate: {},
        }})"#, config.connectivity.serial.as_ref().unwrap().baud_rate)
        } else {
            "None".into()
        },
    );
    
    fs::write(src_dir.join("config.rs"), content)?;
    Ok(())
}

